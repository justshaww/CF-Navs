import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import path from 'node:path'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const rootDir = path.resolve(__dirname, '..')
const publicConfig = path.join(rootDir, 'wrangler.toml')
const localConfig = path.join(rootDir, 'wrangler.local.toml')
const marker = '# Generated by scripts/create-wrangler-ci.mjs from CI environment variables.'

function readRequiredEnv(name) {
  const value = process.env[name]?.trim()
  if (!value) {
    throw new Error(`Missing required environment variable ${name}.`)
  }
  if (value.includes('\n') || value.includes('\r')) {
    throw new Error(`Environment variable ${name} must be a single-line value.`)
  }
  return value
}

function escapeTomlString(value) {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"')
}

if (existsSync(localConfig)) {
  const existing = readFileSync(localConfig, 'utf8')
  const canOverwrite = existing.startsWith(marker) || process.env.CF_NAVS_FORCE_WRANGLER_CI === '1'
  if (!canOverwrite) {
    throw new Error(
      [
        'wrangler.local.toml already exists and was not generated by this CI helper.',
        'Refusing to overwrite it. Set CF_NAVS_FORCE_WRANGLER_CI=1 only if you really want to replace it.',
      ].join('\n'),
    )
  }
}

const d1DatabaseId = readRequiredEnv('CF_NAVS_D1_DATABASE_ID')
const kvNamespaceId = readRequiredEnv('CF_NAVS_KV_NAMESPACE_ID')
const workerName = process.env.CF_NAVS_WORKER_NAME?.trim()

let config = readFileSync(publicConfig, 'utf8')
  .replace('replace-with-your-d1-database-id', d1DatabaseId)
  .replace('replace-with-your-kv-namespace-id', kvNamespaceId)

if (workerName) {
  if (workerName.includes('\n') || workerName.includes('\r')) {
    throw new Error('CF_NAVS_WORKER_NAME must be a single-line value.')
  }
  config = config.replace(/^name\s*=\s*"[^"]+"/m, `name = "${escapeTomlString(workerName)}"`)
}

mkdirSync(path.dirname(localConfig), { recursive: true })
writeFileSync(localConfig, `${marker}\n${config}`, 'utf8')
console.log('Created wrangler.local.toml from CI environment variables.')
